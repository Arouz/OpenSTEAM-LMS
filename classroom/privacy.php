        </p>
        <p>
            Dans ce cas, le ministère de l’éducation nationale est responsable du
            traitement des Données.
        </p>

        <h2>
            Utilisation
        </h2>
        <p>
            ARÉN Éducation se préoccupe de la protection de la vie privée. Il n'y a pas
            de donnée enregistrée qui vous reconnaît personnellement quand vous
            naviguez sur le site. Nous confirmons qu’aucun transfert de donnée à caractère personnel vers des pays tiers ou à des organisations internationales ne sera toléré. ARÉN Éducation utilise des
            fichiers-témoins (« cookies ») pour deux fonctions :
        </p>
        <ul type="disc">
            <li>
                Gérer et faciliter l'accès aux fonctions de commande en ligne et
                l'espace dédié aux membres;
            </li>
            <li>
                Analyser les processus de navigation sur le site afin d'en améliorer
                l'ergonomie et d'en faciliter l'usage.
            </li>
        </ul>
        <p>
            Aucune utilisation commerciale n'est faite de toute donnée recueillie par
            ARÉN Éducation.
        </p>
        <p>
            Les renseignements que vous fournissez à ARÉN Éducation lorsque vous
            complétez une commande, ne servent qu'à établir votre identité et à nous
            permettre de prendre contact avec vous, au besoin. Ces renseignements nous
            servent également pour les analyses statistiques de l'utilisation du site.
            Ces renseignements ne sont utilisés à aucune autre fin et ne sont pas
            fournis à des tiers. Vous pouvez vous désabonner en tout temps en
            communiquant avec notre service à la clientèle.
        </p>

        <h2>
            Hyperliens
        </h2>
        <p>
            Lorsque vous cliquez sur un lien et que vous quittez notre espace Web, les
            renseignements échangés à partir de ce moment-là ne sont plus assujettis à
            notre politique de confidentialité, mais à celle du nouveau site visité.
            Nous vous invitons, par mesure de prudence, à lire la politique de
            confidentialité du site en question.
        </p>

        <h2>
            « Cookies »
        </h2>
        <p>
            Lorsque vous visitez notre site, de petits fichiers textes connus sous le
            nom de « cookies » peuvent être sauvegardés sur le disque dur de votre
            ordinateur. Nous personnalisons le contenu de certaines pages Web selon le
            type de fureteur que vous utilisez ou toute autre information fournie par
            nos « cookies ». Nous n'utilisons pas ces « cookies » pour rechercher des
            informations personnelles dans votre ordinateur. Si vous rejetez les «
            cookies », vous serez toujours capable de visiter notre site Web.
        </p>
        <p>
            Les visiteurs du site sont par ailleurs informés que des « cookies »
            enregistrent certaines informations qui sont stockées dans la mémoire de
            leur disque dur. Ces informations servent à générer des statistiques
            d’audience du site et à proposer des produits et services en fonction des
            précédentes visites. Un message d’alerte peut demander au visiteur, au
            préalable, s’il souhaite accepter les « cookies », ce qu’il peut bien
            entendu refuser. Il est aussi possible de configurer son navigateur pour
            accepter ou refuser les « cookies » du site areneducation.com à tout
            moment.
        </p>
        <p>
            Ces « cookies » ne contiennent pas d’informations confidentielles
            concernant le visiteur.
        </p>

