            nom, prénom, identifiant, adresse électronique, données de localisation
            etc.) est une donnée personnelle (ci-après « Donnée(s) Personnelle(s) » ou
            « Donnée(s) »).
        </p>

        <h2>Dans le cadre du GAR</h2>

        <p>
            Lorsque l’accès à l’Application se fait dans le cadre du Gestionnaire
            d’Accès aux Ressources développé par le ministère de l’éducation nationale
            (ci-après le « GAR »), les traitements de Données à caractère personnel
            liés à l’accès et à l’utilisation de l’Application sont régis par les
            textes qui encadrent le fonctionnement du GAR accessibles à l’adresse
